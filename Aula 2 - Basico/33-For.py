# =============================================================================
# Range Crescente
# =============================================================================
for n in range(0, 10, 1):
    print(n)

# =============================================================================
# Range Descrescente
# =============================================================================
for n in range(10, 0, -1):
    print(n)
    
# =============================================================================
# Pulando mais de 1
# =============================================================================
for n in range(0, 100, 8):
    print(n, end=', ')
    
# =============================================================================
# Uso de continue e break
# =============================================================================
texto = 'Python'
nova_string = ''

for letra in texto:
    if letra == 't':
        continue
    elif letra == 'o':
        nova_string += letra.upper()
        break
    nova_string += letra
    
print(texto, '<>', nova_string)

